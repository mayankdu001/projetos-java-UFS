public class OperadorIncremental {
    public static void main (String [] args){
       int a = 5;
       int b = 9;
       
       a++;
       b--;
       
        System.out.println("A incrementado: "+ a);
        System.out.println("B incrementado: "+b);
        
        a+=20;
        System.out.println("A: "+a);
        b-=6;
        System.out.println("B: "+b);
        
        
              
    }
}
