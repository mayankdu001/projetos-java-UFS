        // s for -1 ele é o ultimo 
        // s for 1 ele é um estado normal 

        estado.add(new Estados(quantEstado, tipo));// criando estados

        System.out.println("q" + estado.get(quantEstado).valor_estado);

        quantEstado++;
//jtlEstados.setSelectionMode("q"+cont);
        //jtaTranzicoes.setText(jtaTranzicoes.getText() +"q"+cont+"\n");
