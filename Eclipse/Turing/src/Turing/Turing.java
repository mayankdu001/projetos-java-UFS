    private void jbtCriarTranzicaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtCriarTranzicaoActionPerformed
        char direcao;
        if (lado) {// testar a direção 
            direcao = 'D';
        } else {
            direcao = 'E';
        }

        estado.get(jcbEstadoSelect.getSelectedIndex()).trans.add(new Trans(ctfAlfabeto.getText().charAt(0),
                ctfSubstituir.getText().charAt(0),
                direcao, jcbProximoestado.getSelectedIndex()));// adicionando tranzicoes 

        //concatenando as tranzicoes
        String tranzicoes = estado.get(jcbEstadoSelect.getSelectedIndex()).cont
                + ":" + "(q" + jcbEstadoSelect.getSelectedIndex() + ","
                + estado.get(jcbEstadoSelect.getSelectedIndex()).trans.get(estado.get(jcbEstadoSelect.getSelectedIndex()).cont).valor_leitura + ")===>("
                + "q" + jcbEstadoSelect.getSelectedIndex()
                + "," + estado.get(jcbEstadoSelect.getSelectedIndex()).trans.get(estado.get(jcbEstadoSelect.getSelectedIndex()).cont).valor_escrita
                + "," + estado.get(jcbEstadoSelect.getSelectedIndex()).trans.get(estado.get(jcbEstadoSelect.getSelectedIndex()).cont).direcao + ","
                + "q" + estado.get(jcbEstadoSelect.getSelectedIndex()).trans.get(estado.get(jcbEstadoSelect.getSelectedIndex()).cont).proxEstado + ")";

        jcbtranzicoes.addItem(tranzicoes);

        System.out.println(tranzicoes);
        estado.get(jcbEstadoSelect.getSelectedIndex()).cont++;// incrementando as tranzicoes 
        buttonGroup2.clearSelection();// limpando os botoes 
        ctfAlfabeto.setText(null);
        ctfSubstituir.setText(null);
        jrbDireita.setSelected(false);// desmarcar o radio button
        jrbEsquerda.setSelected(false);// desmarcar o radio button


    }//GEN-LAST:event_jbtCriarTranzicaoActionPerformed

